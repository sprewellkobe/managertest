function calculateScores(answers) {
    const dimScores = {};
    DIMENSIONS.forEach(d => { dimScores[d.id] = 0; });

    answers.forEach((answer, idx) => {
        const q = QUESTIONS[idx];
        dimScores[q.dimension] += answer;
    });

    const totalScore = Object.values(dimScores).reduce((a, b) => a + b, 0);
    return { dimScores, totalScore };
}

// ===== 获取层级 =====
function getLevel(totalScore) {
    for (const level of LEVELS) {
        if (totalScore >= level.range[0] && totalScore <= level.range[1]) {
            return level;
        }
    }
    return LEVELS[LEVELS.length - 1];
}

// ===== 获取维度解读 =====
function getDimensionAnalysis(dimId, score) {
    const maxScore = dimId === 'communication' ? 20 : 25; // communication只有4题
    const executionMax = dimId === 'execution' ? 20 : maxScore;
    const actualMax = (dimId === 'communication' || dimId === 'execution') ? 20 : 25;
    const ratio = score / actualMax;
    const detail = DIMENSION_DETAILS[dimId];

    if (ratio >= 0.75) {
        return { level: 'high', desc: detail.high, advice: detail.highAdvice };
    } else if (ratio >= 0.5) {
        return { level: 'mid', desc: detail.mid, advice: detail.midAdvice };
    } else {
        return { level: 'low', desc: detail.low, advice: detail.lowAdvice };
    }
}

