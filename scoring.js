// 简单的验证方式：下载码格式 + 校验逻辑
// 格式: MGRXXXX 其中 XXXX 为计算校验值
function verifyDownloadCode(code) {
    code = code.trim().toUpperCase();

    // 格式校验: MGR + 4位字母数字 + 3位数字
    if (!/^MGR[A-Z0-9]{4}\d{3}$/.format) {
        // 使用算法验证：前缀MGR + 中间4位 + 后3位校验
        if (code.length !== 10) return false;
        if (!code.startsWith('MGR')) return false;

        const middle = code.substring(3, 7);
        const checksum = code.substring(7, 10);

        // 校验算法：中间4位字符ASCII码之和 × 7 mod 1000
        let sum = 0;
        for (let i = 0; i < middle.length; i++) {
            sum += middle.charCodeAt(i);
        }
        const expected = String((sum * 7) % 1000).padStart(3, '0');

        return checksum === expected;
    }

    return false;
}

// 生成下载码的工具函数（你在控制台运行来批量生成）
function generateDownloadCodes(count = 20) {
    const codes = [];
    const chars = 'ABCDEFGHJKLMNPQRSTUVWXYZ23456789'; // 去掉容易混淆的字符

    for (let i = 0; i < count; i++) {
        let middle = '';
        for (let j = 0; j < 4; j++) {
            middle += chars[Math.floor(Math.random() * chars.length)];
        }

        let sum = 0;
        for (let j = 0; j < middle.length; j++) {
            sum += middle.charCodeAt(j);
        }
        const checksum = String((sum * 7) % 1000).padStart(3, '0');

        codes.push('MGR' + middle + checksum);
    }

    console.log('=== 生成的下载码 ===');
    codes.forEach(c => console.log(c));
    console.log('=== 共 ' + codes.length + ' 个 ===');

    return codes;
}
